package sample.other;

public class Category {
    private int id;
    private String namecategory;

    public Category(int id, String namecategory) {
        this.id = id;
        this.namecategory = namecategory;
    }

    public int getId() {
        return id;
    }

    public String getNamecategory() {
        return namecategory;
    }
}
